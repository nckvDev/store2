    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {

        if($request->ajax()){

            $data = Product::all();

            return Datatables::make($data)
            ->addIndexColumn()
            ->addColumn('name', function($data){
                return $data['name'];
            })
            ->rawColumns(['name'])
            ->make(true);
        }
        return view('users.personnel.product');
    }

